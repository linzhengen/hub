package main

import (
	"bytes"
	"fmt"
	"go/ast"
	"go/format"
	"go/token"
	"log"
	"os"
	"sort"
	"strings"
	"text/template"

	"golang.org/x/tools/go/packages"
	pluralize "github.com/gertd/go-pluralize"
)

const adapterTemplate = `// Code generated by gen-adapter. DO NOT EDIT.
package persistence

import (
	"context"
	"database/sql"
	"encoding/json"
	"time"

	"github.com/sqlc-dev/pqtype"
	postgressqlc "github.com/linzhengen/hub/v1/server/internal/infrastructure/persistence/postgres/sqlc"
)

{{range .Models}}
// {{.Name}}Model represents a {{.Name}} in the database
type {{.Name}}Model struct {
{{range .Fields}}	{{.Name}} {{.Type}}
{{end}}}
{{end}}

// Querier is a common interface for database operations
type Querier interface {
	WithTx(tx *sql.Tx) Querier

	{{range .Methods}}	{{.Name}}(ctx context.Context, {{.Args}}) {{.Returns}}
{{end}}}

// PostgreSQLQuerier implements Querier for PostgreSQL
type PostgreSQLQuerier struct {
	q *postgressqlc.Queries
}

// NewPostgreSQLQuerier creates a new PostgreSQLQuerier
func NewPostgreSQLQuerier(q *postgressqlc.Queries) *PostgreSQLQuerier {
	return &PostgreSQLQuerier{q: q}
}

func (p *PostgreSQLQuerier) WithTx(tx *sql.Tx) Querier {
	return &PostgreSQLQuerier{q: p.q.WithTx(tx)}
}

{{range .Methods}}
func (p *PostgreSQLQuerier) {{.Name}}(ctx context.Context, {{.Args}}) {{.Returns}} {
{{.Body}}
}
{{end}}

// NewQuerierAdapter creates the appropriate Querier adapter
func NewQuerierAdapter(queries any) Querier {
	if q, ok := queries.(Querier); ok {
		return q
	}
	if q, ok := queries.(*postgressqlc.Queries); ok {
		return NewPostgreSQLQuerier(q)
	}
	return nil
}

// GetQuerier returns a new Querier instance
func GetQuerier(db *sql.DB) Querier {
	return NewPostgreSQLQuerier(postgressqlc.New(db))
}
`

type Method struct {
	Name    string
	Args    string
	Returns string
	Body    string
}

type Model struct {
	Name   string
	Fields []Field
}

type Field struct {
	Name string
	Type string
}

func main() {
	sqlcPath := "internal/infrastructure/persistence/postgres/sqlc"
	cfg := &packages.Config{
		Mode: packages.NeedFiles | packages.NeedSyntax | packages.NeedTypes,
		Dir:  sqlcPath,
	}
	pkgs, err := packages.Load(cfg, "./...")
	if err != nil {
		log.Fatal(err)
	}

	var querierInterface *ast.InterfaceType
	paramsStructs := make(map[string]*ast.StructType)
	sqlcModels := make(map[string]*ast.StructType)

	for _, pkg := range pkgs {
		for _, file := range pkg.Syntax {
			for _, decl := range file.Decls {
				if gd, ok := decl.(*ast.GenDecl); ok && gd.Tok == token.TYPE {
					for _, spec := range gd.Specs {
						if ts, ok := spec.(*ast.TypeSpec); ok {
							if ts.Name.Name == "Querier" {
								if it, ok := ts.Type.(*ast.InterfaceType); ok {
									querierInterface = it
								}
							} else if strings.HasSuffix(ts.Name.Name, "Params") {
								if st, ok := ts.Type.(*ast.StructType); ok {
									paramsStructs[ts.Name.Name] = st
								}
							} else if strings.HasSuffix(ts.Name.Name, "Row") {
								if st, ok := ts.Type.(*ast.StructType); ok {
									sqlcModels[ts.Name.Name] = st
								}
							} else {
								if st, ok := ts.Type.(*ast.StructType); ok {
									sqlcModels[ts.Name.Name] = st
								}
							}
						}
					}
				}
			}
		}
	}

	if querierInterface == nil {
		log.Fatal("Querier interface not found in sqlc package")
	}

	pluralizeClient := pluralize.NewClient()

	// Generate Models for the adapter
	var models []Model
	for name, st := range sqlcModels {
		if name == "bool" || name == "int32" || name == "int64" || name == "string" || name == "DBTX" || name == "Queries" {
			continue
		}
		modelName := strings.TrimSuffix(name, "Row")
		// Skip singularizing to avoid issues with types like 'bool'
		// modelName = pluralizeClient.Singular(modelName)

		model := Model{Name: modelName}
		for _, f := range st.Fields.List {
			fType := getTypeName(f.Type)
			mappedType := mapType(fType)
			for _, n := range f.Names {
				model.Fields = append(model.Fields, Field{Name: n.Name, Type: mappedType})
			}
		}
		models = append(models, model)
	}
	sort.Slice(models, func(i, j int) bool { return models[i].Name < models[j].Name })

	var methods []Method
	for _, m := range querierInterface.Methods.List {
		if len(m.Names) == 0 {
			continue
		}
		sqlcMethodName := m.Names[0].Name
		methodName := sqlcMethodName
		if strings.HasPrefix(methodName, "Delete") {
			prefix := "Delete"
			rest := strings.TrimPrefix(methodName, prefix)
			methodName = prefix + pluralizeClient.Singular(rest)
		} else if strings.HasPrefix(methodName, "Select") {
			prefix := "Select"
			rest := strings.TrimPrefix(methodName, prefix)
			if rest == "UserAuthorizedPolices" {
				methodName = "SelectUserAuthorizedPolicies"
			} else {
				methodName = prefix + pluralizeClient.Singular(rest)
			}
		}

		ft := m.Type.(*ast.FuncType)

		var paramsNames []string
		var paramsTypes []string
		var sqlcParamsExpanded []string

		for i, field := range ft.Params.List {
			if i == 0 { // Skip context
				continue
			}
			typeName := getTypeName(field.Type)
			if strings.HasSuffix(typeName, "Params") {
				st := paramsStructs[typeName]
				for _, f := range st.Fields.List {
					fType := getTypeName(f.Type)
					mappedType := mapType(fType)
					for _, name := range f.Names {
						paramsNames = append(paramsNames, name.Name)
						paramsTypes = append(paramsTypes, mappedType)

						val := name.Name
						switch fType {
						case "sql.NullString":
							val = fmt.Sprintf("sql.NullString{String: %s, Valid: %s != \"\"}", name.Name, name.Name)
						case "sql.NullInt32":
							val = fmt.Sprintf("sql.NullInt32{Int32: %s, Valid: true}", name.Name)
						}

						sqlcParamsExpanded = append(sqlcParamsExpanded, fmt.Sprintf("%s: %s", name.Name, val))
					}
				}
			} else {
				for _, name := range field.Names {
					paramsNames = append(paramsNames, name.Name)
					paramsTypes = append(paramsTypes, mapType(typeName))
				}
			}
		}

		var body bytes.Buffer
		hasMetadata := false
		for _, n := range paramsNames {
			if strings.ToLower(n) == "metadata" {
				hasMetadata = true
			}
		}
		if hasMetadata {
			fmt.Fprintf(&body, "\tm, _ := json.Marshal(Metadata)\n")
		}

		var returns []string
		if ft.Results != nil {
			for _, field := range ft.Results.List {
				returns = append(returns, getTypeName(field.Type))
			}
		}

		if len(returns) == 1 { // Only error
			if len(sqlcParamsExpanded) > 0 {
				fmt.Fprintf(&body, "\treturn p.q.%s(ctx, postgressqlc.%sParams{\n", sqlcMethodName, sqlcMethodName)
				for _, p := range sqlcParamsExpanded {
					if strings.Contains(p, "Metadata:") {
						fmt.Fprintf(&body, "\t\tMetadata: pqtype.NullRawMessage{RawMessage: m, Valid: m != nil},\n")
					} else {
						fmt.Fprintf(&body, "\t\t%s,\n", p)
					}
				}
				fmt.Fprintf(&body, "\t})\n")
			} else {
				argList := ""
				if len(paramsNames) > 0 {
					argList = ", " + strings.Join(paramsNames, ", ")
				}
				fmt.Fprintf(&body, "\treturn p.q.%s(ctx%s)\n", sqlcMethodName, argList)
			}
		} else if len(returns) == 2 { // Result and error
			sqlcResultType := strings.TrimPrefix(returns[0], "[]")
			sqlcResultType = strings.TrimPrefix(sqlcResultType, "*")

			isBasicType := sqlcResultType == "bool" || sqlcResultType == "string" || sqlcResultType == "int32" || sqlcResultType == "int64"

			modelName := strings.TrimSuffix(sqlcResultType, "Row")
			if !isBasicType {
				modelName = modelName + "Model"
			}

			isSlice := strings.HasPrefix(returns[0], "[]")

			sqlcArgs := ""
			if len(sqlcParamsExpanded) > 0 {
				sqlcArgs = fmt.Sprintf(", postgressqlc.%sParams{\n", sqlcMethodName)
				for _, p := range sqlcParamsExpanded {
					if strings.Contains(p, "Metadata:") {
						sqlcArgs += "\t\tMetadata: pqtype.NullRawMessage{RawMessage: m, Valid: m != nil},\n"
					} else {
						sqlcArgs += fmt.Sprintf("\t\t%s,\n", p)
					}
				}
				sqlcArgs += "\t}"
			} else if len(paramsNames) > 0 {
				sqlcArgs = ", " + strings.Join(paramsNames, ", ")
			}

			if isSlice {
				fmt.Fprintf(&body, "\trows, err := p.q.%s(ctx%s)\n", sqlcMethodName, sqlcArgs)
				fmt.Fprintf(&body, "\tif err != nil {\n\t\treturn nil, err\n\t}\n")
				fmt.Fprintf(&body, "\tres := make([]*%s, len(rows))\n", modelName)
				fmt.Fprintf(&body, "\tfor i, row := range rows {\n")
				fmt.Fprintf(&body, "\t\tres[i] = &%s{\n", modelName)
				if st, ok := sqlcModels[sqlcResultType]; ok {
					for _, f := range st.Fields.List {
						fType := getTypeName(f.Type)
						for _, n := range f.Names {
							fmt.Fprintf(&body, "\t\t\t%s: %s,\n", n.Name, mapField("row."+n.Name, fType))
						}
					}
				}
				fmt.Fprintf(&body, "\t\t}\n\t}\n\treturn res, nil\n")
			} else {
				fmt.Fprintf(&body, "\trow, err := p.q.%s(ctx%s)\n", sqlcMethodName, sqlcArgs)
				fmt.Fprintf(&body, "\tif err != nil {\n")
				if isBasicType {
					switch sqlcResultType {
					case "bool":
						fmt.Fprintf(&body, "\t\treturn false, err\n")
					case "string":
						fmt.Fprintf(&body, "\t\treturn \"\", err\n")
					default:
						fmt.Fprintf(&body, "\t\treturn 0, err\n")
					}
				} else {
					fmt.Fprintf(&body, "\t\treturn nil, err\n")
				}
				fmt.Fprintf(&body, "\t}\n")

				if !isBasicType {
					if strings.Contains(modelName, "Resource") || strings.Contains(modelName, "Policy") || strings.Contains(modelName, "Policies") {
						if st, ok := sqlcModels[sqlcResultType]; ok {
							for _, f := range st.Fields.List {
								fType := getTypeName(f.Type)
								if fType == "pqtype.NullRawMessage" {
									fmt.Fprintf(&body, "\tvar metadata map[string]string\n")
									fmt.Fprintf(&body, "\tif row.Metadata.Valid {\n\t\t_ = json.Unmarshal(row.Metadata.RawMessage, &metadata)\n\t}\n")
								}
							}
						}
					}

					fmt.Fprintf(&body, "\treturn &%s{\n", modelName)
					if st, ok := sqlcModels[sqlcResultType]; ok {
						for _, f := range st.Fields.List {
							fType := getTypeName(f.Type)
							for _, n := range f.Names {
								if n.Name == "Metadata" && strings.Contains(modelName, "Resource") {
									fmt.Fprintf(&body, "\t\t\tMetadata: metadata,\n")
								} else {
									fmt.Fprintf(&body, "\t\t\t%s: %s,\n", n.Name, mapField("row."+n.Name, fType))
								}
							}
						}
					}
					fmt.Fprintf(&body, "\t}, nil\n")
				} else {
					fmt.Fprintf(&body, "\treturn row, nil\n")
				}
			}
		}

		var interfaceArgs []string
		for i := range paramsNames {
			interfaceArgs = append(interfaceArgs, fmt.Sprintf("%s %s", paramsNames[i], paramsTypes[i]))
		}

		var interfaceReturns []string
		if len(returns) == 2 {
			sqlcResultType := strings.TrimPrefix(returns[0], "[]")
			sqlcResultType = strings.TrimPrefix(sqlcResultType, "*")
			isBasicType := sqlcResultType == "bool" || sqlcResultType == "string" || sqlcResultType == "int32" || sqlcResultType == "int64"

			modelName := strings.TrimSuffix(sqlcResultType, "Row")
			if !isBasicType {
				modelName = modelName + "Model"
			}

			if strings.HasPrefix(returns[0], "[]") {
				interfaceReturns = append(interfaceReturns, "[]*"+modelName)
			} else {
				if isBasicType {
					interfaceReturns = append(interfaceReturns, modelName)
				} else {
					interfaceReturns = append(interfaceReturns, "*"+modelName)
				}
			}
			interfaceReturns = append(interfaceReturns, "error")
		} else {
			interfaceReturns = returns
		}

		methods = append(methods, Method{
			Name:    methodName,
			Args:    strings.Join(interfaceArgs, ", "),
			Returns: "(" + strings.Join(interfaceReturns, ", ") + ")",
			Body:    body.String(),
		})
	}

	sort.Slice(methods, func(i, j int) bool {
		return methods[i].Name < methods[j].Name
	})

	tmpl, err := template.New("adapter").Funcs(template.FuncMap{
		"toLower": strings.ToLower,
	}).Parse(adapterTemplate)
	if err != nil {
		log.Fatal(err)
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, struct {
		Methods []Method
		Models  []Model
	}{
		Methods: methods,
		Models:  models,
	}); err != nil {
		log.Fatal(err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		log.Printf("Warning: could not format source: %v", err)
		formatted = buf.Bytes()
	}

	if err := os.WriteFile("internal/infrastructure/persistence/adapter_gen.go", formatted, 0644); err != nil {
		log.Fatal(err)
	}
}

func mapType(sqlcType string) string {
	switch sqlcType {
	case "sql.NullString":
		return "string"
	case "sql.NullInt32":
		return "int32"
	case "pqtype.NullRawMessage":
		return "map[string]string"
	}
	return sqlcType
}

func mapField(val, sqlcType string) string {
	switch sqlcType {
	case "sql.NullString":
		return val + ".String"
	case "sql.NullInt32":
		return val + ".Int32"
	}
	return val
}

func getTypeName(expr ast.Expr) string {
	switch t := expr.(type) {
	case *ast.Ident:
		return t.Name
	case *ast.StarExpr:
		return "*" + getTypeName(t.X)
	case *ast.SelectorExpr:
		return getTypeName(t.X) + "." + t.Sel.Name
	case *ast.ArrayType:
		return "[]" + getTypeName(t.Elt)
	}
	return ""
}
